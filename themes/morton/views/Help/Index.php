<h2>Frequently Asked Questions (and their Answers):</h2>
        <p><b>How can I use the images I see here?</b></p>
<p>You can use the digital images in ACORN with proper attribution (see below) for any personal or educational purpose. We make every attempt to ensure that we are abiding by copyright law, but if you use one of these images, you take responsibility for verifying its copyright status. </p>

<p><b>What if I need an image at a higher resolution?</b></p>
<p>
The Arboretum has high resolution (300 dpi) images for nearly all of the images you see online. If we can verify the copyright status, we will be able to provide you with a digital file for a fee based on your intended use. Fill out the <a href="https://goo.gl/forms/48krJM8nXfFj2jjP2">Image Use Request Form</a> to start this process. Typical turnaround is 2-3 weeks. </p>

<p><b>What if an ACORN record doesn’t have an image?</b></p>
<p>
If you see a record in ACORN that does not have an image, it likely means that it has not yet been digitized as part of our ongoing efforts to make available more images from our collections. You may contact the Library to <a href="https://docs.google.com/forms/d/e/1FAIpQLScAxMbn0dxxYsWlUB-bU_vSrYvjzkqic8OVzwKR0V-CLp4VJw/viewform">make an appointment</a> to view the physical collections or contact us to request that the item be prioritized for digitization. A fee for staff time and equipment use may be associated with these requests.</p>

<p><b>What if I need an image for another purpose, or I’m not sure whether it’s okay to use the image?</b></p>
<p>Fill out the <a href="https://goo.gl/forms/48krJM8nXfFj2jjP2">Image Use Request Form</a> with as many details as you can, and we’ll get back to you.</p>

<p><b>What is the proper way to attribute these images?</b></p>
<p>[Copyright Holder], [Creator (if not the copyright holder)], Courtesy of The Morton Arboretum</p>

<p><b>Should I register for a free ACORN account?</b></p>
<p>Registering an account will allow you to use ACORN’s Lightbox feature. If you are an Arboretum staff member, registering an account will give you access to additional resources in ACORN. Please contact <a href="mailto:library@mortonarb.org">library@mortonarb.org</a> for more information on registering your account and gaining additional access. </p>
        
<p><b>What is a Lightbox?</b></p>
<p>After registering and logging into your ACORN account, you will be able to create what is referred to as a Lightbox. A Lightbox, allows users to create, save, and share sets of resources into a single location where you can access them later. Lightboxes can also be shared with other ACORN users. You can access your Lightbox by clicking the profile icon and then selecting Lightbox. </p>
        
