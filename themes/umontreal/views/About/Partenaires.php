	<h1>Partenaires et subventionnaires</h1>
	<br>
	<p>La Collection ethnographique entretient des liens et des partenariats avec différents organismes de conservation, de recherche et de valorisation du patrimoine. Parmi les projets développés au cours des dernières années, notons :</p>
	<ul>
		<li>La contribution, par le versement de données et de photos de notre base de données, aux réseaux Info-Muse (Société des Musées du Québec), Artefacts Canada (Réseau canadien d'information sur le patrimoine) et Reciprocal Research Network (Musée d'anthropologie de l'Université de la Colombie-Britannique).</li>
		<li>Des expositions au Musée de la civilisation (Québec), au Musée canadien de l'histoire (Gatineau), au Musée Redpath de l'Université McGill, au Musée McCord, au Musée du textile et du costume du Québec, à Pointe-du-Buisson, Musée québécois d'archéologie, avec le réseau Archéo-Québec et avec l'Institut culturel Avataq.</li>
		<li>La restauration d'objets et le développement d'outils de conservation préventive par l'Institut canadien de conservation (ICC).</li>
		<li>La fabrication de boîtes pour la mise en réserve par des étudiants du programme de techniques de muséologie du Collège Montmorency.</li>
  	</ul>
