			  <h4>Projet en cours : Valorisation des collections autochtones</h4>
			<p>La collection ethnographique a bénéficié en 2016 et 2017 de subventions du Ministère de l'enseignement supérieur et des communications du Québec pour la valorisation et la numérisation d'une partie de ses collections autochtones d'Amérique du Nord. Les corpus des groupes Innu, Atikamekw et Inuits, ainsi que le fonds de la Guilde canadienne des métiers d'art ont ainsi pu être photographiés et leur documentation révisée et enrichie. La subvention a aussi permi le tranfert des données informatisées de la collection vers une nouvelle base de données, CollectiveAccess, qui comprend une interface de publication en ligne.</p> 
