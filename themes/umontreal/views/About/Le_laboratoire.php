				<p>Principalement utilisée dans le cadre de cours pratiques en anthropologie et en muséologie, cette collection offre aux étudiants un contact privilégié et stimulant avec des témoins de la créativité humaine. Elle permet ainsi d’aborder des thèmes variés touchant à l’étude de la culture matérielle et aux enjeux actuels en ethnomuséologie. La collection se classe parmi les grands ensembles ethnographiques canadiens et reçoit régulièrement des demandes de prêts de la part d’institutions muséales.</p>			
