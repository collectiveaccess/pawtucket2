				<H1>Le laboratoire</H1>
				<div class="content clearfix">
				<br>
				<p>Fondée en 1961 à l'initiative des professeurs Jean Benoist et Guy Dubreuil, la collection d’objets ethnographiques du département d’anthropologie a été constituée grâce aux voyages de recherche de professeurs et d’étudiants, à l'achat de quelques fonds constitués par des spécialistes d'institutions partenaires ainsi qu’aux dons d’un petit nombre de galeries et de particuliers. Elle a pour objectif premier de mettre à la disposition de la communauté universitaire un corpus d’objets reflétant les modes de vie de différentes cultures du monde et comprend aujourd’hui près de 3800 objets provenant des cinq continents et datant de la fin du XIXe siècle à la deuxième moitié du XXe siècle.</p>
