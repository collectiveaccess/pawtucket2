				<br>
				<div class="content clearfix">
				<p>Fondée en 1961, la collection a été principalement constituée dans le courant des années 1960 et 1970 dans le cadre de terrains de chercheurs du département et de commandes à des spécialistes étrangers. Des dons de particuliers s'y sont ajoutés depuis la fin des années 1990.</p>			
				<p>La collection compte aujourd’hui 78 fonds totalisant près de 3800 objets provenant des cinq continents et datant de la fin du XIXe siècle à la deuxième moitié du XXe siècle. Ils se composent principalement d'objets de la vie quotidienne qui documentent les modes de subsistances et la vie domestique, les activités sociales et rituelles étant aussi représentées. Les fonds d’Afrique et d’Amérique sont particulièrement riches, et plusieurs d’entre eux ont fait l'objet de publications.</p>
