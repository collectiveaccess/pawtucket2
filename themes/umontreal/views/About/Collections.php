			  <h4>Amériques</h4>
				<p>Les ensembles autochtones d'Amérique du Nord comptent trois corpus principaux : un corpus des <a href="/Search/objets/search/innu">Innus du Labrador</a> collecté à Sheshatshiu par José Mailhot et Andrée Michaud en 1963, un corpus <a href="/Search/objets/search/atikamekw">atikamekw</a> collecté par Norman Clermont à Wemotaci en 1973 et des collections <a href="/Search/objets/search/inuit+canada">inuites</a> du Nouveau-Québec (B. Saladin d'Anglure, 1966), des Innuinait (Inuits du Cuivre, R. Savard, 1966) et des Netsilingmiut (Inuits de Netsilik, A. Balikci 1962 à 1968). À celles-ci s'ajoute un ensemble d'objets de différents groupes offerts par la <a href="/Search/objets/search/guilde">Guilde des Métiers d'Art</a> en 1970.</p>  
				<p>La collection compte aussi un important corpus nahua et totonaque de la <a href="/Search/objets/search/puebla">Sierra Norte de Puebla</a> collecté par Pierre Beaucage et Madeleine Desnoyers au Mexique en 1970-1971, ainsi que deux ensembles de matériel religieux <a href="/Search/objets/search/haiti">vodou d'Haïti</a> (L. Douyon, 1968; S. Larose, 1970) et des petits corpus de vannerie du <a href="/Search/objets/search/martinique">Morne-des-Esses</a>, en Martinique, (F. Chartrand, 1963) et de <a href="http://collectionethno.umontreal.ca/index.php/Search/objects/search/martinique">Guadeloupe</a> (S. Pharand, 1971).</p>
				<p>L'Amérique du Sud est représentée par plusieurs ensembles du bassin de l'Amazone incluant des objets des <a href="http://collectionethno.umontreal.ca/index.php/Search/objects/search/suriname">groupes bushinengués</a> du Suriname (A. Trouwborst, 1963), <a href="/Search/objets/search/venezuela">piaroas et guahibo</a> du Vénézuela (J. Benoist, 1963), <a href="/Search/objets/search/venezuela">cuiva</a> de Colombie (B. Arcand, 1970) et des <a href="/Search/objets/search/amazonie">Achuar, Asháninka (Campa), Cinta Larga et Karajà</a> d'Amazonie péruvienne et brésilienne (L. Vallée, années 1970 et 1980). De riches collections des <a href="/Search/objets/search/amerique+du+sud+andes">régions andines</a> du Pérou et de Bolivie collectées par Lionel Vallée (années 1960 et 1970), Victoria Von-Hagen (1967) et Jacques Bordaz (1969) comprennent aussi quelques pièces en céramiques et des fragments de textiles archéologiques.</p>
