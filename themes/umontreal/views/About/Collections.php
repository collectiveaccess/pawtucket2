			  <h4>Afrique</h4>
			  <div class="content clearfix">
 				<p>Les principales collections africaines documentent les modes de vie des <a href="/Search/objets/search/bassari">Bassaris du Sénégal</a> (Madeleine Gessain, Musée de l'Homme de Paris, 1963), des <a href="/Search/objets/search/search/bijogo">Bijogo de Guinée-Bissau</a> (Danielle Gallois-Duquette, 1976) et de différents <a href="/Search/objets/search/ethiopie">groupes d'Éthiopie</a> (Asen Balikci, 1969), <a href="/Search/objets/search/afrique+centrale">d'Afrique centrale</a> (Louis Molet, 1965-1967; Daemen, 1968) et de <a href="/Search/objets/search/madagascar">Madagascar</a> (Louis Molet, 1965).</p>
      			<p>À ces collections ethnographiques s'ajoutent depuis quelques années des dons d'objets destinés au marché de l'art occidental ou aux touristes étrangers qui permettent d'illustrer une autre facette de la production matérielle de différents groupes d'Afrique de l'Ouest et d'Afrique centrale (Anne Boisvert, 1994; Jacques Dansereau, 2013).</p>      
