			  <h4>Collections et communautés autochtones</h4>
			<p>Le laboratoire accueille aussi les membres de communautés autochtones désirant obtenir des renseignements sur nos fonds et venir étudier les objets.</p>
			<p>En 2004, à la demande de l'<a href="http://www.avataq.qc.ca">Institut culturel Avataq</a>, trois objets, dont un grand kayak en peau, collectés à Kangiqsujuaq par l'anthropologue Bernard Saladin d'Anglure (Université Laval) en 1965, ont été offerts à la Collection d'art Inuit du Nunavik pour être exposés au <a href="http://www.avataq.qc.ca/fr/content/view/full/318">Centre d'accueil et d'interprétation du Parc national des Pingualuit</a>. Ce processus a donné lieu à de riches échanges sur le rôle de nos collections autochtones dans le contexte de la revalorisation des savoir-faire traditionnels.</p>
			<a href="/media/collectiveaccess/images/site/activites/Article_qajaq_mission_accomplie.pdf">Lire l'article sur le retour du kayak à Kangiqsujuaq (document pdf)</a>
