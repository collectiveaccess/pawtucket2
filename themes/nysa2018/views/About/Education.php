		<h2>Overview/Philosophy</h2>
		<p>Since the early 1980s the New York State Archives has taken an active role in promoting the educational use of historical records (primary sources). First through publications and workshops for New York K-12 educators, and then by adding a grant program for the educational use of local government records, a research award program for students in grades 4-12, and online resources created by teachers for teachers.</p>

		<p>Historical records contain first-person accounts that provide valuable insight into local, state and world events. They are especially powerful when used as educational resources, providing students with: connections to people and events throughout history; unique perspectives that can be analyzed and compared to develop balanced interpretations of the past; and the opportunity to practice and develop valuable inquiry skills.</p> 

		<p>This online education resource includes hundreds of digitized historical photographs, letters, broadsides, maps, legal documents, political cartoons, paintings, and other records that relate to New York, United States and world history. New York teachers selected the documents and developed content to correlate with State Education Department learning standards, common core standards and Social Studies Framework. Historical records are interdisciplinary and the accompanying learning activities reflect this.</p>

		<p><strong>Note:</strong> <i>Information contained in these records reflects the pictures and words of people, places and events over more than 400 years of history. These online resources are contemporary to their time periods. Practices, meanings, and perceptions change over time, and those of only a few generations ago may reflect biases and assumptions no longer accepted. Because of this, it is important to place these resources in the context of when they were created. For teachers and students they provide opportunities for examining multiple perspectives, generating discussions and comparing and contrasting points of view over time.</i></p>
		<h2>How to Use This Resource</h2>
		<p>This resource is designed to offer teachers, and other users, as much flexibility as possible. Of the hundreds of documents on this site, most have an accompanying lesson; some are included in document sets made up of multiple documents related to a theme; and they may also appear in document based questions. Each of these formats offers the user the ability to customize a learning activity. </p>
		<p>Documents and learning activities are accessed by using the search box located on the upper right of each page, or by browsing by topic, lesson type, standard, and more. </p>
		<p>To customize a learning activity: 
			<ul><li>Search or browse for a document and open its lesson.</li>
				<li>Click the "Customize and Download PDF" button at the top of the page.</li>
				<li>Edit or add information using the text boxes.</li>  
				<li>Check the boxes you would like to include in your lesson, and uncheck the boxes you would like to exclude. You can also check the individual pages of the document that you would like to include.</li>
				<li>Click the "download" button at the bottom of the page.</li>
			</ul>
		</p>
		<p>To download document images without lesson information:
			<ul><li>Search or browse for a document and open its lesson.</li>
			<li>Click the image you would like to download.  A large image overlay will pop up.</li>
			<li>Click the "save" icon in the lower right corner.  The image will automatically download.</li>
			</ul>
		</p>
